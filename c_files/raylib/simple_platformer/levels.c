void drawLevels(Levels *levels) {
  for (int i = 0; i < ROW; i++) {
    for (int j = 0; j < COL; j++) {
      if (levels->level[i][j] > 0) {
        int x = j * levels->block_size;
        int y = i * levels->block_size;

        DrawRectangle(x, y, levels->block_size, levels->block_size, GRAY);
      }
    }
  }
}

void updateLevels(Levels *levels) {
  switch (levels->current_level) {
  case 1:
    levels->level = levels->level1;
    break;
  case 2:
    levels->level = levels->level2;
    break;
  case 3:
    levels->level = levels->level3;
    break;
  case 4:
    levels->level = levels->level4;
    break;
  case 5:
    levels->level = levels->level5;
    break;
  }
}
