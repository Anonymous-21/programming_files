void drawLevels(Levels *levels) {
  for (int i = 0; i < ROW; i++) {
    for (int j = 0; j < COL; j++) {
      int x = j * levels->block_size;
      int y = i * levels->block_size;

      if (levels->level[i][j] >= 1 && levels->level[i][j] <= 4) {
        DrawRectangle(x, y, levels->block_size, levels->block_size, GRAY);
      } else if (levels->level[i][j] == 5) {
        DrawRectangle(x, y, levels->block_size, levels->block_size, RED);
      }
    }
  }
}

void updateLevels(Levels *levels) {
  // level unlock
  if (levels->level1_unlocked) {
    levels->level1[15][19] = 0;
    levels->level1[16][19] = 0;
    levels->level1[17][19] = 0;
    levels->level1[18][19] = 0;
    levels->level2[15][0] = 0;
    levels->level2[16][0] = 0;
    levels->level2[17][0] = 0;
    levels->level2[18][0] = 0;
  } else if (levels->level2_unlocked) {
    levels->level1[15][19] = 0;
    levels->level1[16][19] = 0;
    levels->level1[17][19] = 0;
    levels->level1[18][19] = 0;
  } else if (levels->level3_unlocked) {
    levels->level1[15][19] = 0;
    levels->level1[16][19] = 0;
    levels->level1[17][19] = 0;
    levels->level1[18][19] = 0;
  } else if (levels->level4_unlocked) {
    levels->level1[15][19] = 0;
    levels->level1[16][19] = 0;
    levels->level1[17][19] = 0;
    levels->level1[18][19] = 0;
  }

  // update current level
  switch (levels->current_level) {
  case 1:
    levels->level = levels->level1;
    break;
  case 2:
    levels->level = levels->level2;
    break;
  case 3:
    levels->level = levels->level3;
    break;
  case 4:
    levels->level = levels->level4;
    break;
  case 5:
    levels->level = levels->level5;
    break;
  }
}
